// Definición de la función 'sumar' que toma dos parámetros 'a' y 'b'
// Esta función devuelve la suma de 'a' y 'b'
function sumar(a, b) {
    return a + b; // Retorna la suma de los dos números
}

// Llamada a la función 'sumar' con los valores 5 y 7
const resultado = sumar(5, 7);

// Imprimimos el resultado de la suma en la consola
console.log(`La suma es: ${resultado}`); // Debería mostrar: La suma es: 12
